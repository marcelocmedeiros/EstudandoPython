# Operador in e not in

semana=['Segunda', 'Terça', 'Quarta', 'Quinta', 'Sexta' ]
# in está contido
if 'Sexta' in semana:
    print("Sim, ", 'Sexta'," está na lista")
else:
    print("Não está")
# not in não está contido
if 'Domingo' not in semana:
    print("Não, ", 'Domingo'," não está na lista")
else:
    print("Está")   