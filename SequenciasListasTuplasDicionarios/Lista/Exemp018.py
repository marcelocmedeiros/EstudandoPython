# Quebrando e Fatiando Listas - Operador :
# Sintaxe: novaLista = Lista[inicio : fim]

semana=['Segunda', 'Terça', 'Quarta', 'Quinta', 'Sexta', 'Sábado', 'Domingo' ]

novaLista = semana[0:5] 
print(novaLista)
novaLista = semana[:5]
print(novaLista)
novaLista = semana[1:]
print(novaLista)
novaLista = semana[:]
print(novaLista)
novaLista = semana[-1]
print(novaLista)
novaLista = semana[0::2]
print(novaLista)
