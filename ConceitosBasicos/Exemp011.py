# Operações Matemáticas no Python - Adição (+),
# Subtração (-), Multiplicação (*), Divisão (/),
# Exponenciação (**) e Resto da divisão ( % )

# var1 == 5 | var2 == 2
var1 = float( input("Digite um inteiro: ") )
var2 = float( input("Digite outro inteiro: ") )
# Somar em Python: +
soma = var1 + var2
print('Somar:', var1,'+',var2,'=',soma)
# Subtrair em Python: -
subtracao = var1 - var2
print('Subtrair:', var1,'-',var2,'=',subtracao)
# Multiplicar em Python: *
produto = var1 * var2
print('Multiplicar:', var1,'x',var2,'=',produto)
# Dividir em Python: /
divisao = var1 / var2
print('Dividir:', var1,'/',var2,'=',divisao)
# Exponenciação em Python: **
exp = var1 ** var2
print('Exponenciação:', var1,'**',var2,'=',exp)
# Resto da Divisão: %
resto = var1 % var2
print('Resto da Divisão:', var1,'%',var2,'=',resto)
