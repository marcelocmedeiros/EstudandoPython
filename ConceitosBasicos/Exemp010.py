# Operações Matemáticas no Python - Adição (+),
# Subtração (-), Multiplicação (*), Divisão (/),
# Exponenciação (**) e Resto da divisão ( % )

# var1 == 5 | var2 == 2
var1 = int( input("Digite um inteiro: ") )
var2 = int( input("Digite outro inteiro: ") )
# Somar em Python: +
soma = var1 + var2
print('Somar em Python:',soma)
# Subtrair em Python: -
subtracao = var1 - var2
print('Subtrair em Python:',subtracao)
# Multiplicar em Python: *
produto = var1 * var2
print('Multiplicar em Python:',produto)
# Dividir em Python: /
divisao = var1 / var2
print('Dividir em Python:',divisao)
# Exponenciação em Python: **
exp = var1 ** var2
print('Exponenciação em Python:',exp)
# Resto da Divisão: %
resto = var1 % var2
print('Resto da Divisão:',resto)
