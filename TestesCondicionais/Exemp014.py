# função round() é receber um número qualquer e arredondar

numero = float(input('Número original: '))
print("Arredondado :", round(numero))

x = 1.5
print(round(x))

y = 2.5
# Python arredonda p o número para mais próximo
print(round(y))

t = 34.987765
# número é arredondado com 3 casa decimais ou o valor que colocar depois da virgula
print(round(t, 3))

