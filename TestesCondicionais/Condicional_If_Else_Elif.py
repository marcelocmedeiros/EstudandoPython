# condicional com if: - else:

idade = 17 # trocar para 18
if (idade >= 18) :
    print("Pode entrar, a festa está bombando!")
    print("Temos muita música e drinks especiais!!!")
else :
    print("Você é jovem demais para este clube! Volte apenas quando fizer 18.")

# comandos if e else terminam com um sinal de dois pontos “:” e que as linhas subordinadas a cada comando estão indentadas. 
# Em Python  ao contrário do que ocorre em qualquer outra linguagem de programação, os blocos de código que estão subordinados ao if, else e a outros comandos como for e while, são definidos através da indentação de comandos