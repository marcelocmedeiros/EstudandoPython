# Trata-se de uma pequena função anônima que deve possuir apenas uma expressão definida em uma única linha de comando.
# sintaxe: lambda x,y: x + y
# não há a necessidade de utilizar o comando return
# a função possui uma única linha contendo apenas uma expressão
# Com relação ao número de parâmetros, não existe limite, podemos definir quantos a gente quiser

# função lambda
soma = lambda x,y: x + y
print(soma(1,2)) #3
print(soma(5,10)) #15

